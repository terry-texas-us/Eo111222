class TestQueryTrap:
    """Verify the QUERY TRAP pipe verb returns valid JSON geometry data."""

    def test_query_trap_empty(self, pipe: PipeConnection) -> None:
        """QUERY TRAP on an empty trap must return OK with groups==0."""
        # Explicitly clear the trap before querying so prior tests don't pollute state.
        pipe.send("CLEARTRAP")
        r = pipe.send("QUERY TRAP")
        assert is_ok(r), f"Expected OK, got {r!r}"
        data = json.loads(ok_value(r))
        assert data["groups"] == 0
        assert data["primitives"] == 0
        assert data["bbox_area"] == 0.0

    def test_query_trap_after_draw_and_trap(self, pipe: PipeConnection) -> None:
        """Draw a line, trap the work layer, query — should report at least 1 group."""
        # Start clean.
        pipe.send("CLEARTRAP")

        # Draw a horizontal line from (0,0) to (100,0).
        assert is_ok(pipe.send("LINE 0,0 100,0"))

        # Trap everything on the current work layer.
        assert is_ok(pipe.send("TRAPWORK"))

        r = pipe.send("QUERY TRAP")
        assert is_ok(r), f"Expected OK from QUERY TRAP, got {r!r}"

        data = json.loads(ok_value(r))
        assert data["groups"] >= 1,     f"Expected at least 1 trapped group, got {data}"
        assert data["primitives"] >= 1, f"Expected at least 1 primitive, got {data}"
        assert data["bbox_width"] > 0.0, f"Expected non-zero bbox_width, got {data}"

        # Clean up.
        pipe.send("CLEARTRAP")
        pipe.send("UNDO")
        pipe.send("REFRESH")

    def test_query_trap_convenience_api(self, pipe: PipeConnection) -> None:
        """query_trap() returns a dict with the expected keys via the convenience wrapper."""
        from aespy import query_trap
        data = query_trap(pipe)
        assert isinstance(data, dict)
        for key in ("groups", "primitives", "bbox", "bbox_width", "bbox_height",
                    "bbox_area", "items"):
            assert key in data, f"Missing key {key!r} in {data}"

    def test_unknown_query_returns_error(self, pipe: PipeConnection) -> None:
        """An unsupported QUERY sub-command must return ERROR:UNKNOWN_QUERY."""
        r = pipe.send("QUERY NOSUCHVERB")
        assert is_error(r), f"Expected ERROR, got {r!r}"
        assert error_code(r) == "UNKNOWN_QUERY", f"Unexpected error code in {r!r}"


