class TestQueryLayer:
    """Verify the QUERY LAYER <name> pipe verb returns valid JSON layer data."""

    # The default layer present in every new AeSys document.
    _DEFAULT_LAYER = "0"

    def test_no_layer_returns_error(self, pipe: PipeConnection) -> None:
        """Querying a non-existent layer name must return ERROR:NOT_FOUND."""
        r = pipe.send("QUERY LAYER __no_such_layer_xyz__")
        assert is_error(r), f"Expected ERROR for unknown layer, got {r!r}"
        assert error_code(r) in ("NOT_FOUND", "NO_DOC"), (
            f"Unexpected error code: {r!r}"
        )

    def test_missing_name_returns_bad_args(self, pipe: PipeConnection) -> None:
        """QUERY LAYER with no name must return ERROR:BAD_ARGS."""
        r = pipe.send("QUERY LAYER")
        assert is_error(r), f"Expected ERROR for missing name, got {r!r}"
        assert error_code(r) == "BAD_ARGS", f"Unexpected error code: {r!r}"

    def test_query_default_layer_response_keys(self, pipe: PipeConnection) -> None:
        """Querying the default layer '0' returns a dict with all required keys."""
        r = pipe.send(f"QUERY LAYER {self._DEFAULT_LAYER}")
        if is_error(r) and error_code(r) == "NOT_FOUND":
            pytest.skip("Default layer '0' not present — document may be empty")

        assert is_ok(r), f"Expected OK from QUERY LAYER, got {r!r}"
        data = json.loads(ok_value(r))

        for key in ("name", "visible", "is_work", "groups", "primitives", "extents"):
            assert key in data, f"Missing key {key!r} in {data}"

        for ekey in ("min_x", "min_y", "max_x", "max_y"):
            assert ekey in data["extents"], (
                f"Missing extents key {ekey!r} in {data['extents']}"
            )

        assert isinstance(data["name"], str)
        assert isinstance(data["visible"], bool)
        assert isinstance(data["is_work"], bool)
        assert isinstance(data["groups"], int) and data["groups"] >= 0
        assert isinstance(data["primitives"], int) and data["primitives"] >= 0

    def test_query_layer_name_matches_request(self, pipe: PipeConnection) -> None:
        """The returned name must match the requested layer name (case-insensitive)."""
        r = pipe.send(f"QUERY LAYER {self._DEFAULT_LAYER}")
        if is_error(r):
            pytest.skip("Default layer not found")
        data = json.loads(ok_value(r))
        assert data["name"].upper() == self._DEFAULT_LAYER.upper(), (
            f"Returned name {data['name']!r} does not match request"
        )

    def test_query_layer_primitive_count_consistent(
            self, pipe: PipeConnection) -> None:
        """Drawing a line on the work layer increases its primitive count."""
        # Get baseline count for the work layer (draw a line to find it).
        pipe.send("LINE 0,0 10,0")
        time.sleep(0.05)

        # Find which layer was written to by querying layer "0".
        r_before = pipe.send(f"QUERY LAYER {self._DEFAULT_LAYER}")
        if is_error(r_before):
            pipe.send("UNDO")
            pytest.skip("Default layer not found")
        before = json.loads(ok_value(r_before))

        # Draw another primitive.
        pipe.send("LINE 0,1 10,1")
        time.sleep(0.05)

        r_after = pipe.send(f"QUERY LAYER {self._DEFAULT_LAYER}")
        after = json.loads(ok_value(r_after))

        assert after["primitives"] >= before["primitives"], (
            f"Primitive count should not decrease after drawing: "
            f"{before['primitives']} -> {after['primitives']}"
        )

        # Clean up.
        pipe.send("UNDO")
        pipe.send("UNDO")
        pipe.send("REFRESH")

    def test_query_layer_convenience_api(self, pipe: PipeConnection) -> None:
        """query_layer() returns a dict or raises RuntimeError — no TypeError."""
        from aespy import query_layer
        try:
            data = query_layer(pipe, self._DEFAULT_LAYER)
            assert isinstance(data, dict)
            assert "primitives" in data
        except RuntimeError:
            pass  # NOT_FOUND is acceptable if layer doesn't exist

    def test_unknown_query_error_message_lists_layer(
            self, pipe: PipeConnection) -> None:
        """The UNKNOWN_QUERY error detail now mentions LAYER."""
        r = pipe.send("QUERY XYZZY")
        assert is_error(r)
        assert error_code(r) == "UNKNOWN_QUERY"
        assert "LAYER" in r, f"Expected LAYER in error detail: {r!r}"


